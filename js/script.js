// Penyesuaian KM
const diskonKm = { rendah: 0, sedang: 0.05, tinggi: 0.10 };
const labelKm  = {
  rendah: '0–10.000 km',
  sedang: '11.000–15.000 km',
  tinggi: '16.000–20.000 km'
};

let estimasiTerakhir = {};

function fmt(angka) {
  return new Intl.NumberFormat('id-ID', {
    style: 'currency', currency: 'IDR', maximumFractionDigits: 0
  }).format(angka);
}

function hitungEstimasi() {
  const merek = document.getElementById('merek').value;
  const tipe  = document.getElementById('tipe').value;
  const cc    = document.getElementById('cc').value;
  const tahun = document.getElementById('tahun').value;
  const km    = document.getElementById('km').value;

  if (!merek || !tipe || !cc || !tahun || !km) {
    alert('Harap isi semua field terlebih dahulu!');
    return;
  }

  const data  = dataPasar[merek][tipe][cc];
  const range = data[tahun];
  const disc  = diskonKm[km];

  const minHarga = Math.round(range[0] * (1 - disc));
  const maxHarga = Math.round(range[1] * (1 - disc));

  const desc = `${data.label} · Tahun ${tahun} · ${labelKm[km]}`;

  estimasiTerakhir = {
    desc,
    harga: `${fmt(minHarga)} – ${fmt(maxHarga)}`
  };

  document.getElementById('harga-hasil').textContent = `${fmt(minHarga)} – ${fmt(maxHarga)}`;
  document.getElementById('desc-hasil').textContent  = desc;
  document.getElementById('hasil-estimasi').style.display = 'block';
  document.getElementById('hasil-estimasi').scrollIntoView({ behavior: 'smooth', block: 'nearest' });
}

function tanyaWA() {
  const pesan =
`Halo Admin Moto+! 👋

Saya ingin menanyakan motor dengan spesifikasi berikut:
🏍️ *${estimasiTerakhir.desc}*
💰 Estimasi harga: *${estimasiTerakhir.harga}*

Apakah tersedia unit yang sesuai? Terima kasih!`;

  window.open(`https://wa.me/${NOMOR_WA_ADMIN}?text=${encodeURIComponent(pesan)}`, '_blank');
}